     OC语言是1门面向对象的语言.
 
 
     面向过程的解决问题的缺点: 后期的维护和修改不方便.
     面向对象的解决问题的优点: 后期的维护和修改十分方便.
 
 
     使用面向对象设计我们的程序,可以让我们的程序在后期的维护和修改当中更加的方便和快捷.
 
 
 
 
