 3. 代码世界的面向过程与面向对象.
 
    1). 面向过程.
 
    在遇到1个需求的时候,实现这个需求的每1个步骤 都是自己写代码亲自的去一步步的实现. 这样的解决问题的方式我们就叫做面向过程的方式.
    
 
    2). 面向对象.
 
        在遇到1个需求的时候,不要亲自去实现 而是找1个专门做这件事情的人来帮助我们搞定. 这样的解决问题的方式就叫做面向对象的解决方式.
 
 
